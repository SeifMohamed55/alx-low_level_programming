/**
 * op_add - adds two ints
 * @a: f
 * @b: s
 *
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: f
 * @b: s
 *
 * Return: subtract
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: f
 * @b: s
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 * @a: f
 * @b: s
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: f
 * @b: s
 *
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
